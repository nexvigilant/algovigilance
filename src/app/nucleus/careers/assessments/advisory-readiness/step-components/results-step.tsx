'use client';

import { useState } from 'react';
import { Card, CardContent, CardHeader, CardTitle, CardDescription } from '@/components/ui/card';
import { Button } from '@/components/ui/button';
import { Badge } from '@/components/ui/badge';
import { Progress } from '@/components/ui/progress';
import { Tabs, TabsContent, TabsList, TabsTrigger } from '@/components/ui/tabs';
import {
  ChevronLeft,
  RotateCcw,
  Download,
  Copy,
  Check,
  Zap,
  Award,
  Network,
  Rocket,
  Target,
  ArrowRight,
  Lightbulb,
  TrendingUp
} from 'lucide-react';
import {
  ASSESSMENT_DIMENSIONS,
  ADVISORY_PATHWAYS,
  ADVISORY_PRINCIPLES,
  calculateDimensionScore,
  calculateOverallScore,
  getReadinessLevel
} from '../advisory-readiness-data';

interface AllResponses {
  valueProposition: Record<string, number>;
  experience: Record<string, number>;
  network: Record<string, number>;
  readiness: Record<string, number>;
}

interface ResultsStepProps {
  allResponses: AllResponses;
  onBack: () => void;
  onReset: () => void;
}

const DIMENSION_ICONS = {
  'value-proposition': Zap,
  'experience-track-record': Award,
  'network-visibility': Network,
  'readiness-commitment': Rocket
};

const DIMENSION_COLORS = {
  'value-proposition': 'cyan',
  'experience-track-record': 'gold',
  'network-visibility': 'purple-400',
  'readiness-commitment': 'green-400'
};

const RESPONSE_KEYS: Record<string, keyof AllResponses> = {
  'value-proposition': 'valueProposition',
  'experience-track-record': 'experience',
  'network-visibility': 'network',
  'readiness-commitment': 'readiness'
};

export function ResultsStep({ allResponses, onBack, onReset }: ResultsStepProps) {
  const [copied, setCopied] = useState(false);
  const [activeTab, setActiveTab] = useState('overview');

  // Calculate scores
  const dimensionScores: Record<string, number> = {};
  ASSESSMENT_DIMENSIONS.forEach(dim => {
    const responseKey = RESPONSE_KEYS[dim.id];
    dimensionScores[dim.id] = calculateDimensionScore(allResponses[responseKey]);
  });

  const overallScore = calculateOverallScore(dimensionScores);
  const readinessLevel = getReadinessLevel(overallScore);

  // Find strongest and weakest dimensions
  const sortedDimensions = Object.entries(dimensionScores)
    .sort(([, a], [, b]) => b - a);
  const strongestDimension = ASSESSMENT_DIMENSIONS.find(d => d.id === sortedDimensions[0][0]);
  const weakestDimension = ASSESSMENT_DIMENSIONS.find(d => d.id === sortedDimensions[sortedDimensions.length - 1][0]);

  // Generate report content
  const generateReport = () => {
    return `ADVISORY READINESS ASSESSMENT RESULTS
${'='.repeat(50)}

OVERALL SCORE: ${overallScore}/100
READINESS LEVEL: ${readinessLevel.title}

${readinessLevel.description}

DIMENSION SCORES
${'-'.repeat(30)}
${ASSESSMENT_DIMENSIONS.map(dim => {
  const score = dimensionScores[dim.id];
  return `${dim.title}: ${score}/100`;
}).join('\n')}

KEY STRENGTHS
${'-'.repeat(30)}
Your strongest dimension is ${strongestDimension?.title} (${dimensionScores[strongestDimension?.id || '']}/100).
This suggests you have a solid foundation in this area.

DEVELOPMENT PRIORITIES
${'-'.repeat(30)}
Focus on ${weakestDimension?.title} (${dimensionScores[weakestDimension?.id || '']}/100).
${weakestDimension?.description}

RECOMMENDED NEXT STEPS
${'-'.repeat(30)}
${readinessLevel.nextSteps.map((step, i) => `${i + 1}. ${step}`).join('\n')}

TIMELINE: ${readinessLevel.timeframe}

SUGGESTED ADVISORY PATHWAYS
${'-'.repeat(30)}
Based on your profile, consider these advisory pathways:
${ADVISORY_PATHWAYS.slice(0, 2).map(p => `- ${p.title}: ${p.description}`).join('\n')}

---
Generated by AlgoVigilance Academy
Advisory Readiness Assessment`;
  };

  const handleCopy = async () => {
    await navigator.clipboard.writeText(generateReport());
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  const handleDownload = () => {
    const blob = new Blob([generateReport()], { type: 'text/plain' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = 'advisory-readiness-assessment.txt';
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
    URL.revokeObjectURL(url);
  };

  // Get color class based on score
  const getScoreColor = (score: number) => {
    if (score >= 75) return 'text-green-500';
    if (score >= 50) return 'text-gold';
    return 'text-muted-foreground';
  };

  const _getProgressColor = (score: number) => {
    if (score >= 75) return 'bg-green-500';
    if (score >= 50) return 'bg-gold';
    return 'bg-muted-foreground';
  };

  return (
    <div className="space-y-6">
      {/* Header with Overall Score */}
      <Card className="bg-gradient-to-br from-cyan/10 via-purple-500/10 to-gold/10 border-cyan/30">
        <CardContent className="p-6">
          <div className="flex flex-col md:flex-row items-center gap-6">
            {/* Score Circle */}
            <div className="relative w-32 h-32">
              <svg className="w-full h-full transform -rotate-90">
                <circle
                  cx="64"
                  cy="64"
                  r="56"
                  fill="none"
                  stroke="currentColor"
                  strokeWidth="8"
                  className="text-nex-dark"
                />
                <circle
                  cx="64"
                  cy="64"
                  r="56"
                  fill="none"
                  stroke="currentColor"
                  strokeWidth="8"
                  strokeDasharray={`${(overallScore / 100) * 352} 352`}
                  className={getScoreColor(overallScore)}
                />
              </svg>
              <div className="absolute inset-0 flex flex-col items-center justify-center">
                <span className={`text-3xl font-bold ${getScoreColor(overallScore)}`}>{overallScore}</span>
                <span className="text-xs text-muted-foreground">out of 100</span>
              </div>
            </div>

            {/* Level Info */}
            <div className="flex-1 text-center md:text-left">
              <Badge
                className={`mb-2 ${
                  readinessLevel.id === 'established' ? 'bg-green-500' :
                  readinessLevel.id === 'ready' ? 'bg-cyan' :
                  readinessLevel.id === 'developing' ? 'bg-gold' :
                  'bg-muted-foreground'
                } text-white`}
              >
                {readinessLevel.title}
              </Badge>
              <h2 className="text-2xl font-bold text-foreground mb-2">
                Your Advisory Readiness Assessment
              </h2>
              <p className="text-muted-foreground text-sm">
                {readinessLevel.description}
              </p>
              <p className="text-xs text-cyan mt-2 font-semibold">
                {readinessLevel.timeframe}
              </p>
            </div>
          </div>
        </CardContent>
      </Card>

      {/* Action Buttons */}
      <div className="flex flex-wrap gap-3">
        <Button
          variant="outline"
          size="sm"
          onClick={handleCopy}
          className="border-cyan text-cyan hover:bg-cyan/10"
        >
          {copied ? <Check className="h-4 w-4 mr-2" /> : <Copy className="h-4 w-4 mr-2" />}
          {copied ? 'Copied!' : 'Copy Report'}
        </Button>
        <Button
          variant="outline"
          size="sm"
          onClick={handleDownload}
          className="border-nex-border"
        >
          <Download className="h-4 w-4 mr-2" />
          Download
        </Button>
      </div>

      {/* Tabs for Different Views */}
      <Tabs value={activeTab} onValueChange={setActiveTab}>
        <TabsList className="grid grid-cols-4">
          <TabsTrigger value="overview">Overview</TabsTrigger>
          <TabsTrigger value="dimensions">Dimensions</TabsTrigger>
          <TabsTrigger value="pathways">Pathways</TabsTrigger>
          <TabsTrigger value="actions">Actions</TabsTrigger>
        </TabsList>

        {/* Overview Tab */}
        <TabsContent value="overview" className="space-y-4">
          {/* Dimension Scores Grid */}
          <div className="grid md:grid-cols-2 gap-4">
            {ASSESSMENT_DIMENSIONS.map(dim => {
              const score = dimensionScores[dim.id];
              const Icon = DIMENSION_ICONS[dim.id as keyof typeof DIMENSION_ICONS];
              const color = DIMENSION_COLORS[dim.id as keyof typeof DIMENSION_COLORS];

              return (
                <Card key={dim.id} className="bg-nex-surface border-nex-border">
                  <CardContent className="p-4">
                    <div className="flex items-center gap-3 mb-3">
                      <div className={`p-2 bg-${color}/20 rounded-lg`}>
                        <Icon className={`h-5 w-5 text-${color}`} />
                      </div>
                      <div className="flex-1">
                        <p className="text-sm font-semibold text-foreground">{dim.title}</p>
                        <p className={`text-lg font-bold ${getScoreColor(score)}`}>{score}/100</p>
                      </div>
                    </div>
                    <Progress value={score} className="h-2" />
                  </CardContent>
                </Card>
              );
            })}
          </div>

          {/* Characteristics */}
          <Card className="bg-nex-surface border-nex-border">
            <CardHeader>
              <CardTitle className="text-base flex items-center gap-2">
                <Target className="h-5 w-5 text-cyan" />
                Your Profile Characteristics
              </CardTitle>
            </CardHeader>
            <CardContent>
              <ul className="space-y-2">
                {readinessLevel.characteristics.map((char, i) => (
                  <li key={i} className="flex items-start gap-2 text-sm">
                    <span className="text-cyan mt-1">•</span>
                    <span className="text-muted-foreground">{char}</span>
                  </li>
                ))}
              </ul>
            </CardContent>
          </Card>
        </TabsContent>

        {/* Dimensions Tab */}
        <TabsContent value="dimensions" className="space-y-4">
          {/* Strengths & Gaps */}
          <div className="grid md:grid-cols-2 gap-4">
            <Card className="bg-green-500/5 border-green-500/20">
              <CardHeader className="pb-2">
                <CardTitle className="text-sm flex items-center gap-2 text-green-500">
                  <TrendingUp className="h-4 w-4" />
                  Key Strength
                </CardTitle>
              </CardHeader>
              <CardContent>
                <p className="font-semibold text-foreground">{strongestDimension?.title}</p>
                <p className="text-sm text-muted-foreground mt-1">{strongestDimension?.description}</p>
                <Badge className="mt-2 bg-green-500/20 text-green-500">
                  Score: {dimensionScores[strongestDimension?.id || '']}/100
                </Badge>
              </CardContent>
            </Card>

            <Card className="bg-gold/5 border-gold/20">
              <CardHeader className="pb-2">
                <CardTitle className="text-sm flex items-center gap-2 text-gold">
                  <Lightbulb className="h-4 w-4" />
                  Development Priority
                </CardTitle>
              </CardHeader>
              <CardContent>
                <p className="font-semibold text-foreground">{weakestDimension?.title}</p>
                <p className="text-sm text-muted-foreground mt-1">{weakestDimension?.description}</p>
                <Badge className="mt-2 bg-gold/20 text-gold">
                  Score: {dimensionScores[weakestDimension?.id || '']}/100
                </Badge>
              </CardContent>
            </Card>
          </div>

          {/* Key Principles */}
          <Card className="bg-nex-surface border-nex-border">
            <CardHeader>
              <CardTitle className="text-base">Advisory Success Principles</CardTitle>
            </CardHeader>
            <CardContent className="grid md:grid-cols-2 gap-3">
              {Object.values(ADVISORY_PRINCIPLES).map((principle, i) => (
                <div key={i} className="p-3 bg-nex-dark rounded-lg">
                  <p className="text-sm font-semibold text-cyan">{principle.title}</p>
                  <p className="text-xs text-muted-foreground mt-1">{principle.description}</p>
                </div>
              ))}
            </CardContent>
          </Card>
        </TabsContent>

        {/* Pathways Tab */}
        <TabsContent value="pathways" className="space-y-4">
          {ADVISORY_PATHWAYS.map(pathway => (
            <Card key={pathway.id} className="bg-nex-surface border-nex-border">
              <CardHeader>
                <CardTitle className="text-base">{pathway.title}</CardTitle>
                <CardDescription>{pathway.description}</CardDescription>
              </CardHeader>
              <CardContent className="space-y-4">
                <div>
                  <p className="text-xs font-semibold text-muted-foreground mb-2">REQUIREMENTS</p>
                  <ul className="space-y-1">
                    {pathway.requirements.map((req, i) => (
                      <li key={i} className="text-sm text-muted-foreground flex items-start gap-2">
                        <span className="text-cyan">•</span> {req}
                      </li>
                    ))}
                  </ul>
                </div>

                <div>
                  <p className="text-xs font-semibold text-muted-foreground mb-2">PV EXAMPLES</p>
                  <div className="flex flex-wrap gap-2">
                    {pathway.pvExamples.map((ex, i) => (
                      <Badge key={i} variant="outline" className="text-xs">
                        {ex}
                      </Badge>
                    ))}
                  </div>
                </div>

                <div className="flex gap-4 text-xs">
                  <div>
                    <span className="text-muted-foreground">Compensation: </span>
                    <span className="text-foreground">{pathway.compensation}</span>
                  </div>
                  <div>
                    <span className="text-muted-foreground">Time: </span>
                    <span className="text-foreground">{pathway.timeCommitment}</span>
                  </div>
                </div>
              </CardContent>
            </Card>
          ))}
        </TabsContent>

        {/* Actions Tab */}
        <TabsContent value="actions" className="space-y-4">
          <Card className="bg-cyan/5 border-cyan/20">
            <CardHeader>
              <CardTitle className="text-base flex items-center gap-2">
                <Rocket className="h-5 w-5 text-cyan" />
                Your Recommended Next Steps
              </CardTitle>
              <CardDescription>
                Based on your {readinessLevel.title} profile
              </CardDescription>
            </CardHeader>
            <CardContent>
              <ol className="space-y-3">
                {readinessLevel.nextSteps.map((step, i) => (
                  <li key={i} className="flex items-start gap-3">
                    <div className="w-6 h-6 rounded-full bg-cyan/20 flex items-center justify-center flex-shrink-0 mt-0.5">
                      <span className="text-xs font-bold text-cyan">{i + 1}</span>
                    </div>
                    <p className="text-sm text-foreground">{step}</p>
                  </li>
                ))}
              </ol>
            </CardContent>
          </Card>

          {/* Related Assessments */}
          <Card className="bg-nex-surface border-nex-border">
            <CardHeader>
              <CardTitle className="text-base">Continue Your Journey</CardTitle>
              <CardDescription>Complete these related assessments</CardDescription>
            </CardHeader>
            <CardContent className="space-y-3">
              <a
                href="/nucleus/careers/assessments/value-proposition-builder"
                className="flex items-center justify-between p-3 bg-nex-dark rounded-lg hover:bg-nex-dark/80 transition-colors"
              >
                <div className="flex items-center gap-3">
                  <Zap className="h-5 w-5 text-cyan" />
                  <div>
                    <p className="text-sm font-semibold text-foreground">NECS Value Proposition Builder</p>
                    <p className="text-xs text-muted-foreground">Sharpen your professional positioning</p>
                  </div>
                </div>
                <ArrowRight className="h-4 w-4 text-muted-foreground" />
              </a>
              <a
                href="/nucleus/careers/assessments/hidden-job-market"
                className="flex items-center justify-between p-3 bg-nex-dark rounded-lg hover:bg-nex-dark/80 transition-colors"
              >
                <div className="flex items-center gap-3">
                  <Network className="h-5 w-5 text-purple-400" />
                  <div>
                    <p className="text-sm font-semibold text-foreground">Hidden Job Market Navigator</p>
                    <p className="text-xs text-muted-foreground">Access unadvertised opportunities</p>
                  </div>
                </div>
                <ArrowRight className="h-4 w-4 text-muted-foreground" />
              </a>
              <a
                href="/nucleus/careers/assessments/interview-preparation"
                className="flex items-center justify-between p-3 bg-nex-dark rounded-lg hover:bg-nex-dark/80 transition-colors"
              >
                <div className="flex items-center gap-3">
                  <Target className="h-5 w-5 text-gold" />
                  <div>
                    <p className="text-sm font-semibold text-foreground">Interview Preparation Tool</p>
                    <p className="text-xs text-muted-foreground">Research and prepare for conversations</p>
                  </div>
                </div>
                <ArrowRight className="h-4 w-4 text-muted-foreground" />
              </a>
            </CardContent>
          </Card>
        </TabsContent>
      </Tabs>

      {/* Navigation */}
      <div className="flex justify-between items-center pt-4">
        <Button
          variant="outline"
          onClick={onBack}
          className="border-nex-border text-muted-foreground hover:text-foreground"
        >
          <ChevronLeft className="mr-2 h-4 w-4" />
          Back to Readiness
        </Button>
        <Button
          variant="outline"
          onClick={onReset}
          className="border-nex-border text-muted-foreground hover:text-foreground"
        >
          <RotateCcw className="mr-2 h-4 w-4" />
          Start Over
        </Button>
      </div>
    </div>
  );
}
