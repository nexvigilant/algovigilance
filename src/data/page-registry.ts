/** Get pages grouped by section */
export function getPagesBySection(): Record<string, PageEntry[]> {
  const groups: Record<string, PageEntry[]> = {};
  for (const page of PAGE_REGISTRY) {
    if (!groups[page.section]) {
      groups[page.section] = [];
    }
    groups[page.section].push(page);
  }
  return groups;
}

